use Illuminate\Notifications\Messages\MailMessage;
use Illuminate\Notifications\Notification;

class Transfusion extends Notification
{
    use Queueable;

    /**
     * Create a new notification instance.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Get the notification's delivery channels.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function via($notifiable)
    {
        return ['database'];
    }



    /**
     * Get the array representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function toDatabase($notifiable)
    {
        return [
            'message'=>"You are now eligible for donation. If you're willing to donate again, please visit the nearest hospital with a blood bank  for more information",
            'appeal_type'=>"Blood transfusion reminder"
           
        ];
    }
}
